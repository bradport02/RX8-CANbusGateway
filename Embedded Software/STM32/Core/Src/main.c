void I2C_Scan(I2C_HandleTypeDef *hi2c)
{
    char buf[64];
    uint8_t found = 0;

    Report_USB("--- I2C Scan Start ---");

    for (uint8_t addr = 0x08; addr <= 0xEF; addr += 2)
    {
        // HAL_I2C_IsDeviceReady attempts to contact the address
        // 1 trial, 10ms timeout
        if (HAL_I2C_IsDeviceReady(hi2c, addr, 3, 100) == HAL_OK)
        {
            snprintf(buf, sizeof(buf),
                     "Found: 0x%02X (7-bit: 0x%02X)",
                     addr, addr >> 1);
            Report_USB(buf);
            found++;
        }
    }

    if (found == 0)
    {
        Report_USB("No devices found.");
    }
    else
    {
        snprintf(buf, sizeof(buf), "Scan complete. %d device(s) found.", found);
        Report_USB(buf);
    }

    Report_USB("--- I2C Scan End ---");
}

void HAL_FDCAN_RxFifo0Callback(FDCAN_HandleTypeDef *hfdcan1, uint32_t RxFifo0ITs){
	/* - This function is active when an interrupt is fired   -
	 * - Reads the incoming CAN Frame and retransmits on CAN2 - */
	//Report_USB("Frame RX");
	if (hfdcan1->Instance != FDCAN1){
	    return;
	    //if FDCAN2 calls the fifo then it will not run the code.
	}
	if((RxFifo0ITs & FDCAN_IT_RX_FIFO0_NEW_MESSAGE) != RESET){

		FDCAN_RxHeaderTypeDef rxHeader;
		uint8_t	rxData[64];

		//Read the incoming message
		HAL_FDCAN_GetRxMessage(
				hfdcan1,
				FDCAN_RX_FIFO0,
				&rxHeader,
				rxData);

		//Prepare Tx Header (Copy of RX)
		FDCAN_TxHeaderTypeDef txHeader = {
				.Identifier				= rxHeader.Identifier,
				.IdType					= rxHeader.IdType,
				.TxFrameType 			= rxHeader.RxFrameType,
				.DataLength				= rxHeader.DataLength,
				.ErrorStateIndicator	= rxHeader.ErrorStateIndicator,
				.BitRateSwitch			= rxHeader.BitRateSwitch,
				.FDFormat				= rxHeader.FDFormat,
				.TxEventFifoControl		= FDCAN_NO_TX_EVENTS,
				.MessageMarker			= 0
		};

		if (HAL_FDCAN_GetTxFifoFreeLevel(&hfdcan2) > 0){
			//retransmit the can frame
			HAL_FDCAN_AddMessageToTxFifoQ(&hfdcan2, &txHeader, rxData);
			can_sent_flag = 1;
			can_sent_counter++;
		}
		else{
			//report an error if it fails to transmit
			canError++;
			can_drop_counter++;
		}

	}
}

