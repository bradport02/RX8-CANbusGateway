void Startup_Handshake(){
	uint32_t last_send = 0;

	Report_USB("Starting Handshake Sequence...");

	while(!HandshakeESP){
		// Ping the ESP32 every 1000ms (1 second)
		if (HAL_GetTick() - last_send > 1000) {
			Protocol_Send(&huart4, STM, CMD_HANDSHAKE, NULL, 0);
			last_send = HAL_GetTick();
			//Report_USB("Ping sent to ESP32...");
		}

		// Process any incoming packets
		Protocol_CheckFlag(&huart4, &huart5);
	}

	Report_USB("Handshake Complete!");
}

